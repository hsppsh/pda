package s1.pda.logic;

public class Info {
    private String com_cd;
    private String div_cd;
    private String session_id;
    private String id;
    private String nm;

    public String getCom_cd() {
        return com_cd;
    }
    public void setCom_cd(String com_cd) {
        this.com_cd = com_cd;
    }
    public String getDiv_cd() {
        return div_cd;
    }
    public void setDiv_cd(String div_cd) {
        this.div_cd = div_cd;
    }
    public String getSession_id() {
        return session_id;
    }
    public void setSession_id(String session_id) {
        this.session_id = session_id;
    }
    public String getId() {
        return id;
    }
    public void setId(String id) {
        this.id = id;
    }
    public String getNm() {
        return nm;
    }
    public void setNm(String nm) {
        this.nm = nm;
    }
}
